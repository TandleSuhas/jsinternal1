var a= 5;
var b=8;

//Bitwise
document.writeln("The Bitwise OR ", a|b);
document.writeln("The Bitwise AND ", a&b);
document.writeln("The Bitwise XOR ", a^b);
document.writeln("The Bitwise NOT ", ~a);
document.writeln("The Bitwise Left Shift ",a<<b);
document.writeln("The Bitwise Right Shift ",a>>b);

//logical
document.writeln("The logical OR ", a||b);
document.writeln("The logical AND ", a&&b);
document.writeln("The logical NOT ", !a);